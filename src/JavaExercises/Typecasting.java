package JavaExercises;

public class Typecasting {
    public static void main(String[] args){
        int x =1;
        double y=x;

        System.out.println(x);
        System.out.println(y);

        double z = 3.14;
        int w = (int) z;

        System.out.println(z);
        System.out.println(w);
    }
}
