package JavaExercises;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        double x =9.0;
        double y = 2.5;

        System.out.println(a+x);
        System.out.println(y-b);
        System.out.println(a*x);
        System.out.println(y/b);
        System.out.println(x%b);
    }
}
